#include "vm.h"

void						op_and(t_vm *vm , t_proc *p)
{
	int						n;

	n = p->set[2] & p->set[3];
	p->reg[p->set[4]] = n;
		if (n == 0)
		p->carry = 1;
	else
		p->carry = 0;

	(void)vm;
}
