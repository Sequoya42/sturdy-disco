#include "vm.h"

void						op_sti(t_vm *vm , t_proc *p)
{
	int						n;
	int						r;

	n = 0;
	r = 0;
	n = p->pc + ((p->set[3] + p->set[4]) % 512);
	n %= MEM_SIZE;
	r = p->set[2];
	VM(n) = (r >> 24) & 0xFF;
	VM(n + 1) = (r >> 16) & 0xFF;
	VM(n + 2) = (r >> 8) & 0xFF;
	VM(n + 3) = r & 0xFF;

}


