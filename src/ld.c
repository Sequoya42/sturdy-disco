#include "vm.h"

void						op_ld(t_vm *vm , t_proc *p)
{
	int						n;

	n = p->set[2] % IDX_MOD;
	p->reg[p->set[3]] = n;
	if (n == 0)
		p->carry = 1;
	else
		p->carry = 0;
	(void)vm;
}