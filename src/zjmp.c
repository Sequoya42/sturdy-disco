#include "vm.h"

void						op_zjmp(t_vm *vm, t_proc *p)
{
	int						n;

	n = (p->set[1] % IDX_MOD);
	p->old = p->pc;
	if (p->carry == 1)
		p->pc += n - p->next_i;
	(void)vm;
}
