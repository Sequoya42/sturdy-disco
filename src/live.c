#include "vm.h"

void						op_live(t_vm *vm, t_proc *p)
{
	int						n;
	int						i;

	n = p->set[1];
	i = 0;
	while (i < MAX_PLAYERS)
	{
		if (n == (vm->plr[i].n * -1))
		{
			vm->plr[i].live++;
			p->alive++;
		}
		i++;
	}
	vm->cycle->alive++;
}

