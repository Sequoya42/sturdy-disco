#include "vm.h"

void						op_ldi(t_vm *vm , t_proc *p)
{
	int						n;
	int						r;

	r = 0;
	n = (p->set[2] + p->set[3]) % IDX_MOD;
	if (n == 0)
		p->carry = 1;
	else
		p->carry = 0;
	n--;
	r |= (VM(n + 1) << 24);
	r |= (VM(n + 2) << 16);
	r |= (VM(n + 3) << 8);
	r |= (VM(n + 4));
	p->reg[p->set[4]] = r;
}