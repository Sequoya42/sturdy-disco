#include "vm.h"

void						op_or(t_vm *vm , t_proc *p)
{
	int						v1;
	int						v2;

	v1 = get_value(p, 1, 3, vm);
	v2 = get_value(p, 2, 4, vm);
	p->reg[p->set[4]] = (v1 | v2) % IDX_MOD;
	(void)vm;
}
