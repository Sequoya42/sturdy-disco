#include "vm.h"

void						op_lld(t_vm *vm , t_proc *p)
{
	p->reg[p->set[3]] = p->set[2];
	if (p->set[2] == 0)
		p->carry = 1;
	else
		p->carry = 0;
	(void)vm;
}